    //Botão Salvar 
    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        
        jFileChooser2.showSaveDialog(this);
        String fName = jFileChooser2.getSelectedFile().getName();
        String fPath = jFileChooser2.getSelectedFile().getPath();
        FileWriter fw;  
        PrintWriter pw;  
        
        int i=0;
        int j=0;
        int k;
        try{ 
            
            fw = new FileWriter(fPath + ".txt");  
            
            pw = new PrintWriter(fw);          
        
            while(i<jTextArea2.getLineCount()){
            j= jTextArea2.getLineStartOffset(i);
            
            k= jTextArea2.getLineEndOffset(i)-jTextArea2.getLineStartOffset(i);
            if(i==jTextArea2.getLineCount()-1){pw.println(jTextArea2.getText(j, k));}
            else {pw.println(jTextArea2.getText(j, k-1));}
            i++;
            }
            pw.close();  
        }catch(IOException e){  
           System.out.println(e.getMessage());   
        }catch(NullPointerException e){  
        } catch (BadLocationException ex) {  
        Logger.getLogger(ProjetoTCP_UI.class.getName()).log(Level.SEVERE, null, ex);
    }  
        
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    //Botão de Play/Pause
    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
    
    //Se Botão foi pressionado,o label muda para "Pause" e a música toca.
    if(jToggleButton1.isSelected() && playcount == 0){
        jToggleButton1.setText("Pause");
        jButton2.setEnabled(true);
        
        try {
            playcount++;
            dummy = new String(jTextArea2.getText());
            notes = new Note[dummy.length()];
            for(int i=0;i<dummy.length();i++){
                //System.out.println(dummy.charAt(i));
                notes[i]= new Note(dummy.charAt(i));
                                             }
        
            sequence = new Sequence(Sequence.PPQ, 16);
            track = sequence.createTrack( );  // Begin with a new track
            player.addTrack(track,midi,instrument,notes);
            sequencer = MidiSystem.getSequencer( );
            sequencer.open( );  
            //synthesizer = MidiSystem.getSynthesizer( );
            //synthesizer.open( );
            //sequencer.getTransmitter( ).setReceiver(synthesizer.getReceiver( ));

            // Specify the sequence to play, and the tempo to play it at
            sequencer.setSequence(sequence);
            sequencer.setTempoInBPM(instrument.tempo);

            //sequencer.start();
           
            // Pra saber quando a música acaba.
            sequencer.addMetaEventListener(new MetaEventListener( ) {
                public void meta(MetaMessage m) {
                    // quando a música acabar,o botão de play é resetado!
                    if (m.getType( ) == midi.getEndOfTrack()){
                        
                        jToggleButton1.setText("Play");
                        jToggleButton1.setSelected(false);
                        playcount = 0; 
                        sequencer.stop();
                        jButton2.setEnabled(false);
                        
                    };
                                                }
                                                                    }
                                          );
            // And start playing now.
            
            sequencer.start( );
            
            
        //txt= new TXT(jTextArea2);
        }catch(InvalidMidiDataException ex){
         Logger.getLogger(ProjetoTCP_UI.class.getName()).log(Level.SEVERE, null, ex);
        }catch(MidiUnavailableException ex){
         Logger.getLogger(ProjetoTCP_UI.class.getName()).log(Level.SEVERE, null, ex);
    }
               
               
    }
    //Se o botão play for pressionado no meio da musica,
    //a musica toca a partir de onde parou!
    else if(jToggleButton1.isSelected()){
        sequencer.start(); jToggleButton1.setText("Pause");}
 
    //se o botão pause for pressionado,a musica para.
    else {sequencer.stop(); jToggleButton1.setText("Play");}
    
    }//GEN-LAST:event_jToggleButton1ActionPerformed
